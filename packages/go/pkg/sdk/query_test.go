func TestAnd(t *testing.T) {
	n := &models.Node{
		Status:   models.StatusInProgress,
		Priority: models.PriorityHigh,
	}
	pred := sdk.And(sdk.StatusIs("in-progress"), sdk.PriorityIs("high"))
	if !pred(n) {
		t.Error("expected And to match")
	}

	pred2 := sdk.And(sdk.StatusIs("in-progress"), sdk.PriorityIs("low"))
	if pred2(n) {
		t.Error("And should not match when one predicate fails")
	}
}

func TestOr(t *testing.T) {
	n := &models.Node{Status: models.StatusTodo}
	pred := sdk.Or(sdk.StatusIs("todo"), sdk.StatusIs("blocked"))
	if !pred(n) {
		t.Error("expected Or to match")
	}

	pred2 := sdk.Or(sdk.StatusIs("done"), sdk.StatusIs("blocked"))
	if pred2(n) {
		t.Error("Or should not match when no predicates match")
	}
}

func TestNot(t *testing.T) {
	n := &models.Node{Status: models.StatusDone}
	pred := sdk.Not(sdk.StatusIs("done"))
	if pred(n) {
		t.Error("Not(done) should not match a done node")
	}
	pred2 := sdk.Not(sdk.StatusIs("todo"))
	if !pred2(n) {
		t.Error("Not(todo) should match a done node")
	}
}

