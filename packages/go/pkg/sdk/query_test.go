func TestQueryExecuteWithPredicates(t *testing.T) {
	s := newTestSDK(t)

	// Create test features with different statuses/priorities.
	_, err := s.Features.Create("Alpha Feature",
		sdk.FeatWithPriority("high"), sdk.FeatWithStatus("in-progress"))
	if err != nil {
		t.Fatalf("create Alpha: %v", err)
	}
	_, err = s.Features.Create("Beta Feature",
		sdk.FeatWithPriority("low"), sdk.FeatWithStatus("todo"))
	if err != nil {
		t.Fatalf("create Beta: %v", err)
	}
	_, err = s.Features.Create("Gamma Feature",
		sdk.FeatWithPriority("high"), sdk.FeatWithStatus("todo"))
	if err != nil {
		t.Fatalf("create Gamma: %v", err)
	}

	// Find high-priority features.
	nodes, err := s.Find("features").
		Where(sdk.PriorityIs("high")).
		Execute()
	if err != nil {
		t.Fatalf("Execute: %v", err)
	}
	if len(nodes) != 2 {
		t.Fatalf("expected 2 high-priority, got %d", len(nodes))
	}

	// Find todo + high.
	nodes, err = s.Find("features").
		Where(sdk.StatusIs("todo")).
		Where(sdk.PriorityIs("high")).
		Execute()
	if err != nil {
		t.Fatalf("Execute: %v", err)
	}
	if len(nodes) != 1 {
		t.Fatalf("expected 1 todo+high, got %d", len(nodes))
	}
	if nodes[0].Title != "Gamma Feature" {
		t.Errorf("expected Gamma, got %q", nodes[0].Title)
	}
}

func TestQueryOrderBy(t *testing.T) {
	s := newTestSDK(t)

	_, _ = s.Features.Create("Charlie", sdk.FeatWithPriority("low"))
	_, _ = s.Features.Create("Alpha", sdk.FeatWithPriority("high"))
	_, _ = s.Features.Create("Bravo", sdk.FeatWithPriority("medium"))

	// Sort by title ascending.
	nodes, err := s.Find("features").
		OrderBy("title", sdk.Asc).
		Execute()
	if err != nil {
		t.Fatalf("Execute: %v", err)
	}
	if len(nodes) < 3 {
		t.Fatalf("expected 3 nodes, got %d", len(nodes))
	}
	if nodes[0].Title != "Alpha" {
		t.Errorf("first should be Alpha, got %q", nodes[0].Title)
	}
	if nodes[2].Title != "Charlie" {
		t.Errorf("last should be Charlie, got %q", nodes[2].Title)
	}

	// Sort by priority descending (critical > high > medium > low).
	nodes, err = s.Find("features").
		OrderBy("priority", sdk.Desc).
		Execute()
	if err != nil {
		t.Fatalf("Execute: %v", err)
	}
	if string(nodes[0].Priority) != "high" {
		t.Errorf("first should be high priority, got %q", nodes[0].Priority)
	}
	if string(nodes[len(nodes)-1].Priority) != "low" {
		t.Errorf("last should be low priority, got %q", nodes[len(nodes)-1].Priority)
	}
}

